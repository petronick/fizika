<h2>INTERNO OZRAČIVANJEIVANJE</h2>
<p>U proseku, dve trećine efektivne ekvivalentne doze koju ljudi primaju iz prirodnih izvora potiče od
radioaktivnih materija u vazduhu koji udišu, hrani koju jedu i vodu koju piju. Veoma malo, zaista, od ove
interne doze potiče od radioaktivnih materijala – kao što su ugljenik–14 i tricijum koji se stvaraju usled
kosmičkog zračenja. Gotovo čitava interna doza potiče od zemljinih izvora. U proseku, ljudi primaju oko
180 mikrosiverta godišnje iz kalijuma–40, koji telo apsorbuje uporedo sa ne radioaktivnim kalijumom,
elementom od bitnog značaja. Ali, neuporedivo najveća količina potiče iz elemenata koji nastaju
raspadanjem uranijuma–238, a u manjoj meri iz raspadanja torijum–232. Neki od ovih, kao olovo–210 i
polonijum–210, uglavnom ulaze u telo zajedno sa hranom. Jedan i drugi koncentrisani su u ribi i
rakovima; ljudi koji jedu velike količine hrane iz mora nužno će primiti odgovarajuće visoke doze
radijacije.</p>
<p>Na desetine hiljada stanovnika krajnjeg severa hrani se uglavnom mesom irvasa (ili karibua). U telu ovih
životinjanalaze su velike koncentracije ova dva radioaktivna materijala, naročito polonijuma–210, zbog
toga što one zimi pasu lišajeve u kjim se te radioaktivne materije nagomilavaju. Tako se događa da u
organizam stanovnika ovih krajeva dospevaju količine polonijuma –210 koje su do 35 puta veće od
normalnih. U međuvremenu, na drugom kraju sveta, ljudi koji žive u oblasti Zapadne Australije bogatoj
uranijumom, primaju, jedući meso i iznutrice ovaca i kengura, doze radijacije koje su do 75 puta veće od
normalne. </p>
<p>Radioaktivne matrije kao što su ove često prolaze zaobilaznim putevima kroz prirodnu sredinu pre nego
što dospeju do čoveka. Takvi putevi ili »staze« često se uzimaju u obzir prilikom izračunavanja doza
primljenih od pojedinih izvora. </p>