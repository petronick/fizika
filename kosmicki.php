<h2>KOSMIČKIKI ZRACI</h2>
<p>Nešto manje od polovine covekove izloženosti spoljnoj prirodnoj radijaciji potice od kosmickih zraka.
(Tako su izracunate doze od kosmickih zraka, koje prima osoba koja leti preko Atlantika supersonicnim
avionom od 40 milisiverta, a supsonicnim primi 50 milisiverta). Vecina tih zralka dopire do nas iz dubine
meduzvezdanog prostora; neke emituje Suce prlikom svojih eksplozija. Kosmicki zraci direktno ozracuju
Zemlju, stupajuci u interakciju sa atmosferom, pri cemu se stvaraju ove vrste radijacije i razni
radioaktivni materijal.</p>
<p>Nijedno mesto na Zemlji ne može da izbegne ovaj univerzalni, nevidljivi pljusak. Ali, on neke delove
zemaljske kugle jače pogađa nego druge. Polovi primaju više radijacije nego ekvatorijalni regioni, zbog
toga što magnetsko polje Zemlje skreće naelektrisane čestice sadržane u zračenju. I, što je još značajnije,
nivo radijacije se povećava sa nadmorskom visinom, pošto na velikim visinama ima manje vazduha koji
deluje i kao štit. </p>
<p>Neko ko živi na nivou mora, prima godišnje efektivnu ekvivalentnu dozu od oko 300 mikrosiverta
(milioniti delovi siverta), dok onaj ko živi na visini iznad 2 000 metara prima nekoliko puta veću dozu. </p>
<p>Leteći avionim, putnici i osoblje izloženi su i većim dozama, mada za kraće vreme. Na visimana između
4 000 metara – a to je visina najviših stalno naseljenih šerpaskih sela na obroncima Mont Everesta – i 12
000 metara – koliko iznosi najveća visina interkontinentalnih letova – izloženost kosmičkoj radijaciji
povećava se oko 25 puta. Ona se dalje povećava na visinama između 12 000 i 20 000 metara, koliko je
maksimalna visina leta supersoničnog aviona (slika 3.). </p>
<p>Na leti iz Njujorka u Pariz putnik je izložen radijaciji od oko 50 mikrosiverta u normalnom mlaznom
avionu, a za 20 odsto manjoj, u supersoničnom avionu; iako je supersonični avion izložen intenzivnijem
zračenju, on mnogo brže obavlja putovanje. Sve u svemu, let avionom ima za posledicu kolektivnu
efektivnu ekvivalentnu dozu ozračavanja stanovništva sveta u iznosu odoko 2 000 čovek/siverta godišnje. </p> 

