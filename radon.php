<h2>RADON</h2>
<p>Naučnici su tek nedavno počeli da shvataju da je najvažniji od svih izvora prirodne radijacije nevidljivi
gas bez ukusa i mirisa, sedam i po puta teži od vazduha, zvani radon. Procenjuje se da, zajedno sa svojim
»kćerima« - radionuklidima koji se formiraju prilikom njegovog radioaktivnog raspadanja – radon
normalno doprinosi oko tri četvrtine godišnje efektivne ekvivalentne doze koju pojedinci primaju iz
zemaljskih izvora – i oko polovine onih doza koje oni primaju iz svojih prirodnih izvora zajedno. Većinu
ovih doza oni primaju udisanjem radionuklida, naročito u zatvorenom prostoru. </p>
<p>Radon, u stvarai, ima dva glavna oblika: radon–222, jedan od radionuklida u porodici koja nastaje
prilikom raspadanja uranijuma–238, i radon–220, iz porodice koja nastaje raspadanjem torijuma–232.
Izgleda da radon-222 ima dvadeset puta jače delovanje od radona–220, ali, iz praktičnih razloga, ovde
ćemo obe vrste ovog radioaktivnog gasa zvati »radonom«. U stvar, najveći deo doza radijacije potiče od
radionuklida koji nastaje raspadanjem radona, a ne od samog gasa radona. </p>
<p>Širom sveta, radon izbija iz zemlje, ali nivo zračenja u vazduhu na otvorenom prostoru znatno varira od
mesta do mesta. Pa ipak, ma koliko to izgledalo paradoksalno, ljudi su najviše izloženi radonu u
zatvorenim prostorijama. U delovima sveta s umerenom klimom, koncentracije radona su u zatvorenim
prostorijama prosečno oko osam puta veće nego napolju. U tropskim zemljama nisu ivršena merenja, ali
pošto je tamo klima topla, to su zgrade mnogo više otvorene, pa verovatno ne postoji velika razlika
između koncentracija radona unutra i napolju. Radon se koncentriše u sobnom vazduhu u zgradama koje
se pretežno sastoje od zatvorenih prostorija. Kada taj gas jednom uđe, probijajući se kroz drvene podove 
iz tla ili, u manjoj meri, iz samog građevinskog materijala, on će teško izaći. Ishod toga može da bude
vrlo visok nivo radijacije, naročito ako zgrada podignuta na izrazito radioaktivnom tlu ili ako je
sagrađena od naročito radioaktivnog materijala. Velike količine izolacionog materijala još više
pogoršavaju situaciju, otežavajući radonu da se probije napolje. Sve češće i češće nailazmo na vrlo visoke
nivoe radijacije. Krajem sedamdesetih godina, koncentracije koje 5 000 puta premašuju tipične nivoe
radijacije u vazduhu na otvorenom prostoru, registrovane su u stanovima u Švedskoj i Finskoj. Postoje
primeri da su u Britaniji i SAD nađene u nekim stanovima koncentracije za 500 puta veće od tipičnog
nivoa na otvorenom prostoru, a od tog vremena su u ovim dvema zemljama u nekim stanovima
registrovani nivoi radona ravni onim najvišim koji su zabeleženi u Skandinaviji. Uporedo sa povećanjem
broja stanova u kojima su izvršena takva merenja, povećao se i broj onih za koje je utvrđeno da sadrže
ekstremne koncentracije radona. </p>