<h2>RADIOAKTIVNO ZAGAĐIVANJE I ZAŠTITA</h2>
<p>Iako su jonizujuća zračenja i radioaktivnost otkriveni još krajem prošlog stoleća, zračenje kao oblik
kontaminacije životne sredine počinje naglo da zabrinjava široku svetsku javnost tek posle užasa
atomskog bombardovanja Japana 1945. godine. Ovu zabrinutost povećavaju probe novog nuklearnog
oružja i ranije gotovo nepoznata pojava radioaktivnih padavina. Široka primena radijacija i
radioaktivnosti, korišćenje nuklearne energije, ali i opasni kvarovi u nuklearnim elektranama u novije
vreme, pored svih pozitivnih tekovina, alarmantno ukazuju i na ozbiljne ekološke i zdravstvene posledice
nastalih kontaminacija.</p>
<p>Pojam zračenja ili radijacija (lat. radius – zrak) podrazumeva emisiju zračenja ili čestica iz nekog
izvora. Zračenja vrlo visoke energije, koja su u stanju da direktno ili indirektno stvaraju jone, nazivaju se
jonizujuća zračenja. Jonizujuća zračenja predstavljaju glavni uzrok »povreda« protoplazme koje nastaju
u materiji koja ih apsorbuje. Ove radijacije nastaju u nuklearnim reakcijama i procesima, kao i posebnim
laboratorijskim i industrijskim uređajima, a prisutna su i u kosmičkom zračenju. Radioaktivnost je
osobina nekih hemijskih elemenata, odnosno materija, da emituju nevidljive čestice ili zrake velike
energije. Izotopi elemenata koji emituju jonizujuća zračenja zovu se radioizotopi ili radionuklidi.</p>
<p>Nuklearna energija se oslobađa u nuklearnim reakcijama pretvaranjem mase u energiju. Izvori
jonizujućeg zračenja su:</p>

<ul>
	<li>alfa-čestice, krupne, pozitivno naelektrisane čestice,</li>
	<li>beta-čestice, sitnije, negativno naelektrisane čestice (elektroni) i</li>
	<li>gama-zraci, neutralni elektromagnetni talasi vrlo malih talasnih dužina (Sl. 1).</li>
</ul>
<p>Gama – zracima su vrlo slični X-zracima, koji se dobijaju iz rendgen aparata. Značajni su i neutroni,
krupne nenaelektrisane elementarne čestice, sastavni delovi atomskih jezgara koji kada su izvan njih
postaju radioaktivni.</p>
<p>Prema poreklu i izvoru, zračenja mogu biti prirodna i veštačka. Najveći deo ukupnog zračenja koje prima
svetsko stanovništvo vodi poreklo od prirodnih izvora. Postoje tri osnovna izvora prirodne ili osnovne
radijacije:</p>

<ul>
	<li>kosmička radijacija,</li>
	<li>zemaljska ili radijacija iz Zemljine kore i</li>
	<li>zračenje iz radioaktivnih izvora koji se nalaze u tkivimaživih bića. </li>
</ul>

