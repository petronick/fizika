<p>Jedinica za merenje radioaktivnosti izvora naziva se bekerel (Bq). Jedan bekerel odgovara jednom
raspadu bilo kog radionukleida u jednoj sekundi. Doze zračenja se mere količinom energije apsorbovane
tkivima izloženih zračenju. Jedinica za merenje apsorbovane doze jonizujućeg zračenja, odnosno
energije unete radijacijom po gramu tkiva, naziva se grej (Gy). Jedan grej predstavlja količinu energije
unete jonizujućeg zračenja u jedinicu mase neke materije.</p>
<p>Velike doze koje organizmi prime u kratkim vremenskim intervalima (minutima ili satima) nazivaju se
akutne doze. Nasuprot njima, hroničnim dozama subletalne radijacije nazivaju se one doze koje se mogu
primati stalno tokom celog života. </p>