<p>Prva dva se nazivaju spoljašnjim, a treći unutrašnjim izvorom zračenja u odnosu na čovekov
organizam. U celini, zemaljski izvori imaju najveći udeo u izloženosti čoveka prirodnoj radijaciji.
Zemaljska radioaktivnost potiče od prirodnih radioaktivnih elemenata koji se nalaze u zemljištu, posebno
u glinovitoj podlozi i stenama, i različita je na različitim delovima Zemlje. Naročito je velika iznad
naslaga uranove rude.</p>
<p>Tokom poslednjih nekoliko decenija čovek je nuklearnim fisijama (odnosno cepanjem atomskih jezgara)
proizveo nekoliko stotina veštačkih radioizotopa. Osnovni izvori veštačkog zračenja su: nuklearni
reaktori, nuklearne elektrane, zatim rendgenski aparati, kao i nuklearno oru/je korišćeno prilikom
testiranja. Svi ovi veštački izvori radioaktivnosti znatno su uvećali ukupne doze zračenja koje prima
svaki pojedinac i čovečanstvo u celini. Procenjuje se da je čovek svojim aktivnostima u nuklearnoj
energetici već dodao Zemlji radioaktivnost veću nego što su je sadržale njena prirodna atmosfera i
hidrosfera, a da svi današnji nuklearni reaktori odgovaraju ukupnoj radioaktivnosti tla sa kojim se
neposredno dolazi u dodir.</p> 

