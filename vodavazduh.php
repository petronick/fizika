<h2>IZVORI RADIOAKTIVNOG ZAGAÐIVANJA ZEMLJIŠTA, VODE I
VAZDUHA</h2>
<p>Radioaktivnost je svojstvo jezgra atoma nekih elemenata da spontano emituju kompleksno zracenje, pri
cemu jezgra koja zrace prolaze u druga, tako da na kraju nastaje stabilan elemenat. Ovakva definicija
važii za prirodnu i za veštacku radioaktivnost. Kada je u pitanju prirodna radioaktivnost, covek je
posmatrac, ustanovaljava i analizuje pojavu, dok se veštacka radioaktivnost izaziva eksperimentisanjem.</p>
<p>Radioaktivnost je nukleusna, a ne atomska pojava, jer se odvija u jezgru atoma. Do danas je poznato oko
šezdeset prirodno radiokativnih elemenata i svi oni imaju atomski broj veci od 84 (polonijum).</p>
<p>Radioaktivnost je spontana i jezgra se dezintegrišu (raspadaju) u jedinici vremena. Ovaj period je razlicit
kod razlicitih elemenata. Emisija zracenja traje sve do k jezgro zraci, a kad ono postane stabilno, proces
se zaustavlja, jer jezgro više ne zraci. Covek i priroda u celini su u stalnom radioaktivno mambijentu. To
je posledica prirodne radioaktivnosti.</p>
<p>Postoje cetiri glavna faktora radioaktivnosti: kosmicko zracenje (fotoni, protoni, elektroni, mezoni);
zagadenost u zemlji i atmosferi (prirodno radioaktivne stene, tablica 1); supstance sadržane u ljudskom
telu (kalijum 40, tragovi radijuma i njegovih produkata i ugljenik 14) i nuklearni otpaci (nastaju pri
nuklearnim eksplozijama). Imaju, uglavnom, kratak period poluraspada, ali postoje i oni s dugim
periodom, kao ugljenik 14 (5700 godina), cezijum 137 (30 godina), stroncijum 90 (28 godina),
prometeum 147 (dve godine), rutenijum 106 (jedna godina).</p>
<p>Za radioaktivne pojave važe sledeci zakoni.</p> 